# Part I: Function basics
# So far have been working on python scripts that are long lists of instructions. While this can certainly get the job done,
# for more complicated programs, this won't quite do. For any sizeable project, a long list of commands is almost impossible
# to use as it becomes hard to track variables, their values, control flow and bugs. In addition, such code is very difficult
# to maintain or update. What is the solution then? Functions. Functions are blocks of code with names that you can call 
# elsewhere in a python file. Consider the example below:

def helloworld():
  print("Hello World")
  
